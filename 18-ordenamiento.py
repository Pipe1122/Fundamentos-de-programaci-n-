# Crear la lista y darle valores.
lista_base = [24, 12, 45, 2, 37, 60, 34, 8]
print("Lista base:", lista_base)
# Ordenar lista por medio de una funciÃ³n de Python
lista_base.sort() # Ordenar lista de menor a mayor
