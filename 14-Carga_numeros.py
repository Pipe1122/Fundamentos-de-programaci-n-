# Codificar un programa que solicite la carga de un valor positivo y nos 
# muestre desde 1 hasta el valor ingresado de uno en uno.

n = int (input("Ingrese el valor final:"))
x = 1
while x<=n:
    print(x)
    x = x + 1
    